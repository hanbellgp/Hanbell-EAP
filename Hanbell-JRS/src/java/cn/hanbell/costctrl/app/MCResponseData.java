package cn.hanbell.costctrl.app;

/**
 *
 * @author Administrator
 */
public class MCResponseData {

    protected int code;
    protected String msg;
    private Object data;

    public MCResponseData() {

    }

    public MCResponseData(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
    
    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

}
